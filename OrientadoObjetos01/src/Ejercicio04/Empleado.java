package Ejercicio04;

public class Empleado {
    private String dni;
    private String nombre;
    private String apellidos;
    private int salario;

    public Empleado(String dni, String nombre, String apellidos, int salario) {
        this.dni = dni;
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.salario = salario;
    }


}
